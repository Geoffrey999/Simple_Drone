/* USER CODE BEGIN 1 */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
    if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE))
    {
        __HAL_UART_CLEAR_IDLEFLAG(&huart1);
        
        HAL_UART_DMAStop(&huart1);
        
        uint16_t recv_len = RX_FRAME_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
		uint16_t checksum_rx = (Usart1DmaBuf[31]<<8) | Usart1DmaBuf[30];
		uint16_t checksum_calc=0;
		for(int i=0;i<30;i++)
		{
			checksum_calc += Usart1DmaBuf[i];
		}
		checksum_calc = 0xffff - checksum_calc;
        if (recv_len == RX_FRAME_SIZE && (Usart1DmaBuf[0]==0x20) && (Usart1DmaBuf[1]==0x40) && (checksum_rx==checksum_calc))
        {
            BaseType_t xHigherPriorityTaskWoken = pdFALSE;
            xStreamBufferSendFromISR(usart1RxStreamBuf, Usart1DmaBuf, recv_len, &xHigherPriorityTaskWoken);
            portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
        }

        
        HAL_UART_Receive_DMA(&huart1, Usart1DmaBuf, RX_FRAME_SIZE);
    }
  /* USER CODE END USART1_IRQn 0 */
    HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}
